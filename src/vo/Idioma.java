package vo;

/**
 *
 * @author ThyagoPacher
 */
public class Idioma {
    private String nome;
    private String caminho;
    
    /**
     *
     * @return
     */
    public String getNome() {
        return nome;
    }

    /**
     *
     * @param nome
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     *
     * @return
     */
    public String getCaminho() {
        return caminho;
    }

    /**
     *
     * @param caminho
     */
    public void setCaminho(String caminho) {
        this.caminho = caminho;
    }
}
